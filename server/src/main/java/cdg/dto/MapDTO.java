package cdg.dto;

public class MapDTO {
	private String state;
	private String geoJson;
	
	public String getState()
	{
		return state;
	}
	public void setState(String state)
	{
		this.state = state;
	}
	public String getGeoJson()
	{
		return geoJson;
	}
	public void setGeoJson(String geoJson)
	{
		this.geoJson = geoJson;
	}
}
